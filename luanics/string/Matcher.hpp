#pragma once

#include <string>

namespace luanics::string {

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///
/// @class Matcher
///
/// @brief Predicate for string matching.
///
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
class Matcher {
public:
	virtual ~Matcher() {}

	/// @returns true if matches
	virtual bool operator()(std::string const & target) const = 0;
}; // class Matcher

} // namespace luanics::string
