#include <ios>

namespace luanics {

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///
/// @class IosState
///
/// @brief Used for capturing and restoring state of std::ios and children.
///
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
class IosState {
public:
	IosState() : _state{nullptr} {}

	void capture(std::ios const & stream) { _state.copyfmt(stream); }
	void restore(std::ios & stream) { stream.copyfmt(_state); }

private:
	std::ios _state;
}; // class IosState

}  // namespace luanics
