#pragma once

#include "luanics/logging/Filter.hpp"

namespace luanics {
namespace logging {

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///
/// @class NoFilter
///
/// @brief A filter that accepts all records.
///
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
class NoFilter : public Filter {
public:
	virtual bool isAccepting(Record const & record) const override final {
		return true;
	}
}; // class NoFilter

}  // namespace logging
}  // namespace luanics
